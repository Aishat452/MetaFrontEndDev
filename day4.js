// The for and the while loop tasks

for (let i = 1; i <= 5; i++){

   console.log(i);

};

console.log("Counting has been completed!");

for (let i = 5; i > 0; i--){

   console.log(i);

};

console.log('Countdown done');


// the while loop

while (i < 6){

   console.log(i);
   i++;

};

console.log("counting completed");
